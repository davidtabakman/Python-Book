# -*- coding: utf-8 -*-


class Person(object):

    def __init__(self, name='David', age=17):
        self.__name = name
        self.__age = age

    def get_name(self):
        return self.__name

    def get_age(self):
        return self.__age

    def set_name(self, name):
        self.__name = name

    def set_age(self, age):
        self.__age = age